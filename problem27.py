# オイラーは驚くべき二次式を発見しました：

# n² + n + 41

# この式は、連続する整数値 0 ≤ n ≤ 39 に対して 40 個の素数を生成します。しかし、n = 40 のとき、40² + 40 + 41 = 40(40 + 1) + 41 は 41 で割り切れ、さらに n = 41 のときも 41² + 41 + 41 は明らかに 41 で割り切れます。

